#include<iostream>
using namespace std;
//Tests arithmetic operators:
int main()
{
int m = 38, n = 5;
cout << m << " + " << n << " = " << (m + n) <<endl;
cout << m << " - " << n << " = " << (m - n) << endl;
cout << " -" << n << " = " << (-n) << endl;
cout << m << " * " << n << " = " << (m * n) << endl;
cout << m << " / " << n << " = " << (m / n) << endl;
cout << m << " % " << n << " = " << (m % n) << endl;
return 0;
}

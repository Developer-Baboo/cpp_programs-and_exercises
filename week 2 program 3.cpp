#include<iostream>		// preprocessor
using namespace std;
int main()
{
	int a;				// Integer type variable declaration 
	a=10;				// Assigning value to variable (initialization)
	int b=10;			// Declaration+initialization
	int c;				
	c=a+b;
	cout<<"a="<<a<<endl;
	cout<<"b="<<b<<endl;
	cout<<"c=a+b="<<c<<endl;
	cout<<"a+b="<<a+b<<endl;
	cout<<"a+b+c="<<a+b+c<<endl;
	return 0;
}
